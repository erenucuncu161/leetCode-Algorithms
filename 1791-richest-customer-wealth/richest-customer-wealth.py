class Solution(object):
    def maximumWealth(self, accounts):
        enbuyuk=0
        for i in range(len(accounts)):
            sum1=0
            for j in range(len(accounts[i])):
                sum1+=accounts[i][j]
                if enbuyuk<sum1:
                    enbuyuk=sum1





        
        
        
        
        
                  
        


        
        return enbuyuk
            
                